/**
 * 다음 칸 계산. firstStepBranch는 분기점에서 이동을 시작하는 첫 스텝에만 전달:
 * 5·10은 1=지름길, 22는 1=횡단(15방향)/0=출구(0방향).
 */
export function yutNextNode(
  cur: number,
  cameFrom: number | null,
  firstStepBranch: 0 | 1 | null,
): number {
  if (cur === 5 && firstStepBranch !== null) return firstStepBranch === 1 ? 20 : 6;
  if (cur === 10 && firstStepBranch !== null) return firstStepBranch === 1 ? 25 : 11;
  if (cur === 22) {
    if (firstStepBranch !== null) return firstStepBranch === 1 ? 23 : 27;
    return cameFrom === 26 ? 27 : 23;
  }
  return YUT_NEXT[cur];
}

